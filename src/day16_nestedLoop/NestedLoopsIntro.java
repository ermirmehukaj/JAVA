package day16_nestedLoop;

public class NestedLoopsIntro {

    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) {
            System.out.println("WppdenSpoon");
        }

        System.out.println("------------------------");
        for (int i = 0; i < 5; i++) {

            for (int j = 0; j < 5; j++) {
                System.out.println("WppdenSpoon");
            }

        }



    }
}
