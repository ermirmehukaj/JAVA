public class WeekDays {
    public static void main(String[] args) {

        int number = 2;
        String result = "";

        if(number ==1){
          result = "Monday";
        }
        if(number == 2){
            result = "Tuesday";
        }
        if(number == 3){
           result = "Wednesday";
        }
        System.out.println(result);
    }
}
